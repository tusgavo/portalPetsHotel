            Mas o que é uma vacina ética?<br><br>
            Muitos se referem a vacinas éticas como sendo as vacinas importadas, contudo, a vacina ética é aquela aplicada pelo médico veterinário.<br><br>
            <strong>Nenhum outro profissional</strong> está autorizado a aplicar vacinas. <br><br>
            Mas você pode pensar: poxa! É apenas uma picadinha, qual o problema de uma vacina ser aplicada por outro profissional? <br><br>
            Realmente é apenas uma picadinha, contudo, a saúde geral do animal deve ser cuidadosamente analisada. <br><br>

            O médico veterinário irá verificar se o animal possui alguma doença que impeça a vacina de ser aplicada, por exemplo, algumas doenças que causam queda na imunidade…<br><br>

            Ou até mesmo medicamentos que também causam queda na imunidade podem ser empecilhos para a vacinação. Ou ainda animais mais idosos e muito jovens também podem ter problemas em relação a imunidade. <br><br>
            Por esses e outros motivos, as vacinas <strong>somente podem ser aplicadas por médicos veterinários</strong>. <br><br>

            Além disso, existem particularidades em especial nos felinos que requerem que a aplicação seja feita com a responsabilidade e a cautela que somente um médico veterinário será capaz de avaliar, aplicar corretamente e diminuir as chances de problemas posteriores… <br><br>

